<h4>Comparison operators</h4>
<blockquote>The PHP comparison operators are used to compare two values (number or string):</blockquote>

<code>x == y</code>
<code>x === y</code>
<code>x != y</code>
<code>x !== y</code>
<code>x > y</code>
<code>x < y</code>
<code>x >= y</code>
<code>x <= y</code>
<code>x <=> y</code>

<br/>
<br/>

<h4>Increment/decrement operators</h4>
<blockquote>The PHP increment operators are used to increment/decrement a variable's value.</blockquote>

<code>++$x</code>
<code>$x++</code>
<code>--$x</code>
<code>$x++</code>

<br/>
<br/>

<h4>Logical operators</h4>
<blockquote>The PHP logical operators are used to combine conditional statements.</blockquote>

<code>&&</code>
<code>||</code>
